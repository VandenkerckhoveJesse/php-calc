<?php


class Calculator
{
    public static function Add (int $x, int $y):int {
        return $x + $y;
    }

    public static function Subtract (int $x, int $y):int {
        return $x - $y;
    }

    public static function Multiply (int $x, int $y):int {
        return $x * $y;
    }

    public static function Divide (int $x, int $y):int {
        return $x / $y;
    }

    public static function Power (int $ground, int $exp):int {
        return $ground;
    }


}